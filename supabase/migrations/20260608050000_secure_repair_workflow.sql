begin;

alter table public.repair_requests
  add column if not exists assigned_technician_id uuid references public.profiles(id) on delete set null,
  add column if not exists quote_amount numeric(12, 2),
  add column if not exists quote_description text,
  add column if not exists quote_status text not null default 'not_issued',
  add column if not exists quote_decided_at timestamptz;

alter table public.repair_requests
  drop constraint if exists repair_requests_quote_amount_check,
  drop constraint if exists repair_requests_quote_status_check;

alter table public.repair_requests
  add constraint repair_requests_quote_amount_check
    check (quote_amount is null or quote_amount >= 0),
  add constraint repair_requests_quote_status_check
    check (quote_status in ('not_issued', 'pending', 'accepted', 'rejected'));

create index if not exists repair_requests_created_at_idx
  on public.repair_requests(created_at desc);

create index if not exists repair_requests_assigned_technician_idx
  on public.repair_requests(assigned_technician_id);

create table if not exists public.repair_status_history (
  id bigint generated by default as identity primary key,
  repair_request_id uuid not null references public.repair_requests(id) on delete cascade,
  previous_status text,
  new_status text not null,
  changed_by uuid references public.profiles(id) on delete set null,
  created_at timestamptz not null default now()
);

create index if not exists repair_status_history_repair_created_idx
  on public.repair_status_history(repair_request_id, created_at);

create or replace function public.record_repair_status_history()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
begin
  if tg_op = 'INSERT' then
    insert into public.repair_status_history (
      repair_request_id,
      previous_status,
      new_status,
      changed_by
    )
    values (
      new.id,
      null,
      new.status,
      auth.uid()
    );
  elsif new.status is distinct from old.status then
    insert into public.repair_status_history (
      repair_request_id,
      previous_status,
      new_status,
      changed_by
    )
    values (
      new.id,
      old.status,
      new.status,
      auth.uid()
    );
  end if;

  return new;
end;
$$;

drop trigger if exists repair_status_history_on_change on public.repair_requests;
create trigger repair_status_history_on_change
after insert or update of status on public.repair_requests
for each row execute function public.record_repair_status_history();

insert into public.repair_status_history (
  repair_request_id,
  previous_status,
  new_status,
  changed_by,
  created_at
)
select
  request.id,
  null,
  request.status,
  null,
  request.created_at
from public.repair_requests request
where not exists (
  select 1
  from public.repair_status_history history
  where history.repair_request_id = request.id
);

alter table public.repair_status_history enable row level security;

drop policy if exists "Users edit received repair requests" on public.repair_requests;
drop policy if exists "Staff manage repair requests" on public.repair_requests;
drop policy if exists "Clients edit initial repair data" on public.repair_requests;

create policy "Clients edit initial repair data"
on public.repair_requests for update
to authenticated
using (
  user_id = auth.uid()
  and status = 'received'
)
with check (
  user_id = auth.uid()
  and status = 'received'
);

drop policy if exists "Users read repair history" on public.repair_status_history;
create policy "Users read repair history"
on public.repair_status_history for select
to authenticated
using (
  public.is_staff()
  or exists (
    select 1
    from public.repair_requests request
    where request.id = repair_request_id
      and request.user_id = auth.uid()
  )
);

revoke all on public.repair_status_history from anon, authenticated;
grant select on public.repair_status_history to authenticated;

revoke update on public.repair_requests from authenticated;

revoke update (
  equipment_type,
  brand_model,
  problem_description,
  status,
  technician_notes,
  assigned_technician_id,
  quote_amount,
  quote_description,
  quote_status,
  quote_decided_at
) on public.repair_requests from authenticated;

grant update (
  equipment_type,
  brand_model,
  problem_description
) on public.repair_requests to authenticated;

create or replace function public.admin_update_repair(
  p_repair_id uuid,
  p_status text,
  p_technician_notes text default null,
  p_quote_amount numeric default null,
  p_quote_description text default null,
  p_assigned_technician_id uuid default null
)
returns public.repair_requests
language plpgsql
security definer
set search_path = ''
as $$
declare
  current_request public.repair_requests;
  updated_request public.repair_requests;
  next_quote_status text;
begin
  if not public.is_staff() then
    raise exception 'Only staff can manage repair requests'
      using errcode = '42501';
  end if;

  if p_status not in (
    'received',
    'diagnosis',
    'quoted',
    'repairing',
    'ready',
    'delivered',
    'cancelled'
  ) then
    raise exception 'Invalid repair status'
      using errcode = '22023';
  end if;

  select *
  into current_request
  from public.repair_requests
  where id = p_repair_id
  for update;

  if not found then
    raise exception 'Repair request not found'
      using errcode = 'P0002';
  end if;

  if p_assigned_technician_id is not null and not exists (
    select 1
    from public.profiles
    where id = p_assigned_technician_id
      and role in ('technician', 'admin')
  ) then
    raise exception 'Assigned user is not staff'
      using errcode = '22023';
  end if;

  if p_quote_amount is null and nullif(btrim(p_quote_description), '') is null then
    next_quote_status := 'not_issued';
  elsif p_quote_amount is null or nullif(btrim(p_quote_description), '') is null then
    raise exception 'Quote amount and description are both required'
      using errcode = '22023';
  elsif current_request.quote_amount is distinct from p_quote_amount
     or current_request.quote_description is distinct from btrim(p_quote_description) then
    next_quote_status := 'pending';
  else
    next_quote_status := current_request.quote_status;
  end if;

  update public.repair_requests
  set
    status = case
      when next_quote_status = 'pending'
        and p_status in ('received', 'diagnosis')
        then 'quoted'
      else p_status
    end,
    technician_notes = nullif(btrim(p_technician_notes), ''),
    assigned_technician_id = p_assigned_technician_id,
    quote_amount = p_quote_amount,
    quote_description = nullif(btrim(p_quote_description), ''),
    quote_status = next_quote_status,
    quote_decided_at = case
      when next_quote_status in ('accepted', 'rejected')
        then current_request.quote_decided_at
      else null
    end
  where id = p_repair_id
  returning * into updated_request;

  return updated_request;
end;
$$;

create or replace function public.respond_to_repair_quote(
  p_repair_id uuid,
  p_decision text
)
returns public.repair_requests
language plpgsql
security definer
set search_path = ''
as $$
declare
  updated_request public.repair_requests;
begin
  if p_decision not in ('accepted', 'rejected') then
    raise exception 'Quote decision must be accepted or rejected'
      using errcode = '22023';
  end if;

  update public.repair_requests
  set
    quote_status = p_decision,
    quote_decided_at = now()
  where id = p_repair_id
    and user_id = auth.uid()
    and quote_status = 'pending'
  returning * into updated_request;

  if not found then
    raise exception 'Pending quote not found for current user'
      using errcode = '42501';
  end if;

  return updated_request;
end;
$$;

revoke all on function public.admin_update_repair(
  uuid,
  text,
  text,
  numeric,
  text,
  uuid
) from public;

revoke all on function public.respond_to_repair_quote(uuid, text) from public;
revoke all on function public.record_repair_status_history() from public;

grant execute on function public.admin_update_repair(
  uuid,
  text,
  text,
  numeric,
  text,
  uuid
) to authenticated;

grant execute on function public.respond_to_repair_quote(uuid, text)
to authenticated;

commit;
